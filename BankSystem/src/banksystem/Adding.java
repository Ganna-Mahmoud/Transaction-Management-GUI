
package banksystem;


public class Adding { 
     private String n ;
    private String  i ;
     private String b ;

    public void setN(String n) {
        this.n = n;
    }

    public void setI(String i) {
        this.i = i;
    }

    public void setB(String b) {
        this.b = b;
    }

    public String getN() {
        return n;
    }

    public String getI() {
        return i;
    }

    public String getB() {
        return b;
    }

    @Override
    public String toString() {
        return "Adding{" + "n=" + n + ", i=" + i + ", b=" + b + '}';
    }

  

}
   
    
    
  
    

  
    

