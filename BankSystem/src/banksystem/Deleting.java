
package banksystem;

public class Deleting {
      private String N ;
    private String  I ;
     private String B ;

    public void setN(String N) {
        this.N = N;
    }

    public void setI(String I) {
        this.I = I;
    }

    public void setB(String B) {
        this.B = B;
    }

    public String getN() {
        return N;
    }

    public String getI() {
        return I;
    }

    public String getB() {
        return B;
    }

    @Override
    public String toString() {
        return "Deleting{" + "N=" + N + ", I=" + I + ", B=" + B + '}';
    }
     
    
}
